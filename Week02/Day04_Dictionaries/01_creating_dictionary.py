# Creating Dictionaries in Python

student = {
    "name": "Milind",
    "roll_no": 101,
    "course": "AIML",
    "marks": 89
}

print("Student Dictionary:")
print(student)

print("\nStudent Name:")
print(student["name"])

print("\nCourse:")
print(student["course"])

print("\nMarks:")
print(student["marks"])